package org.theta4j.plugin;

public enum ExitStatus {
    SUCCESS(ThetaIntent.PLUGIN_EXIT_STATUS_SUCCESS),
    FAILURE(ThetaIntent.PLUGIN_EXIT_STATUS_FAILURE);

    private final String value;

    ExitStatus(String value) {
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    @Override
    public String toString() {
        return value;
    }
}
